        function navigateToScreen(screen) {
            // Update active screen
            document.querySelectorAll('.screen').forEach(s => s.classList.remove('active'));
            document.getElementById(`${screen}-screen`)?.classList.add('active');

            // Update active nav item
            document.querySelectorAll('.nav-item').forEach(item => item.classList.remove('active'));
            document.querySelector(`[data-screen="${screen}"]`)?.classList.add('active');

            currentScreen = screen;
        }

        function openChordDetail(titulo) {
            currentChord = cifrasData.find(c => c.titulo === titulo);
            if (!currentChord) return;

            // Update detail screen
            document.getElementById('detail-title').textContent = currentChord.titulo;
            document.getElementById('detail-artist').textContent = currentChord.artista;
            document.getElementById('detail-key').textContent = `Tom: ${currentChord.tom}`;
            document.getElementById('detail-rating').textContent = currentChord.rating;

            // Set chord content
            const defaultChord = currentChord.cifra || generateDummyChord();
            document.getElementById('chord-content').innerHTML = defaultChord;

            currentKey = currentChord.tom;

            // Show detail screen
            document.querySelectorAll('.screen').forEach(s => s.classList.remove('active'));
            document.getElementById('chord-detail-screen').classList.add('active');
        }

