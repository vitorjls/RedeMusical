        // Search and Filter
        function filterChords(query) {
            const filtered = cifrasData.filter(cifra => 
                cifra.titulo.toLowerCase().includes(query.toLowerCase()) ||
                cifra.artista.toLowerCase().includes(query.toLowerCase())
            );

            const grid = document.getElementById('chord-grid');
            if (filtered.length === 0) {
                grid.innerHTML = '<div class="empty-state"><div class="empty-icon">üéµ</div><div class="empty-title">Nenhuma cifra encontrada</div><div class="empty-description">Tente buscar com outros termos</div></div>';
            } else {
                grid.innerHTML = filtered.map(cifra => `
                    <div class="chord-card" data-id="${cifra.titulo}">
                        <div class="chord-card-title">${cifra.titulo}</div>
                        <div class="chord-card-artist">${cifra.artista}</div>
                        <div class="chord-card-info">
                            <div class="chord-key">${cifra.tom}</div>
                            <div class="chord-rating">
                                <span class="material-symbols-outlined star">star</span>
                                ${cifra.rating}
                            </div>
                        </div>
                    </div>
                `).join('');

                // Re-add click listeners
                document.querySelectorAll('.chord-card').forEach(card => {
                    card.addEventListener('click', () => {
                        const titulo = card.dataset.id;
                        openChordDetail(titulo);
                    });
                });
            }
        }

