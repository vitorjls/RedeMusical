        // Chord Controls
        function changeKey(direction, inPresentation = false) {
            const keys = ['C', 'C#', 'D', 'D#', 'E', 'F', 'F#', 'G', 'G#', 'A', 'A#', 'B'];
            let currentIndex = keys.indexOf(currentKey);
            currentIndex = (currentIndex + direction + keys.length) % keys.length;
            currentKey = keys[currentIndex];

            // Update display
            if (inPresentation) {
                document.getElementById('presentation-key').textContent = `Tom: ${currentKey}`;
            } else {
                document.getElementById('detail-key').textContent = `Tom: ${currentKey}`;
            }

            // Show notification
            if (inPresentation) {
                showNotification(`Tom alterado para ${currentKey} para todos os conectados`);
            }
        }

        function changeFontSize() {
            fontSize = fontSize === 14 ? 16 : fontSize === 16 ? 18 : 14;
            document.getElementById('chord-content').style.fontSize = `${fontSize}px`;
        }

